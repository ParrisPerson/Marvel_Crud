    static let mainTintColor = UIColor(named: "mainTintColor")!
    
    static let errorColor = UIColor(named: "errorColor")!
    
    static let menuSelectedColor = UIColor(named: "menuSelectedColor")!
    static let menuGradientStartColor = UIColor(named: "menuGradientStartColor")!
    static let menuGradientEndColor = UIColor(named: "menuGradientEndColor")!
    
    static let toastBackColor = UIColor(named: "toastBackColor")!
    static let toastSuccessColor = UIColor(named: "toastSuccessColor")!
    
    static let blurColor = UIColor(named: "blurColor")!
    
    static let thumbColor = UIColor(named: "thumbColor")!
    static let thumbTintColor = UIColor(named: "thumbTintColor")!
}
